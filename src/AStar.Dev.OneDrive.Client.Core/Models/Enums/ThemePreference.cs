    /// </summary>
    OriginalLight,

    /// <summary>
    ///     Original theme in dark mode.
    /// </summary>
    OriginalDark,

    /// <summary>
    ///     Professional theme with corporate minimalist design.
    /// </summary>
    Professional,

    /// <summary>
    ///     Colourful theme with playful gradients and glass morphism.
    /// </summary>
    Colourful,

    /// <summary>
    ///     Terminal/Hacker theme with retro green-on-black aesthetic.
    /// </summary>
    Terminal
}
